def visualize_boxes_and_labels_on_image_array(
    image,
    boxes,
    classes,
    scores,
    category_index,
    instance_masks=None,
    instance_boundaries=None,
    keypoints=None,
    keypoint_scores=None,
    keypoint_edges=None,
    track_ids=None,
    use_normalized_coordinates=False,
    max_boxes_to_draw=20,
    min_score_thresh=.5,
    agnostic_mode=False,
    line_thickness=4,
    mask_alpha=.4,
    groundtruth_box_visualization_color='black',
    skip_boxes=False,
    skip_scores=False,
    skip_labels=False,
    skip_track_ids=False):
    """Minimal version: draws bounding boxes and class labels on image."""
    import cv2
    height, width, _ = image.shape
    for i in range(min(boxes.shape[0], max_boxes_to_draw)):
        if scores is not None and scores[i] < min_score_thresh:
            continue
        box = boxes[i]
        if use_normalized_coordinates:
            y1, x1, y2, x2 = (box[0]*height, box[1]*width, box[2]*height, box[3]*width)
        else:
            y1, x1, y2, x2 = box
        y1, x1, y2, x2 = int(y1), int(x1), int(y2), int(x2)
        class_id = int(classes[i])
        color = (0, 255, 0)
        label = category_index.get(class_id, {'name': str(class_id)})['name']
        score = scores[i] if scores is not None else None
        label_text = f"{label}: {score:.2f}" if score is not None else label
        cv2.rectangle(image, (x1, y1), (x2, y2), color, line_thickness)
        cv2.putText(image, label_text, (x1, max(y1-10, 0)), cv2.FONT_HERSHEY_SIMPLEX, 0.5, color, 2)
    return image
